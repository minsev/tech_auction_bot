from aiogram import Router, F
from aiogram.types import Message
from aiogram.filters import Command
from keyboards import main_menu
from database import get_user_role, decode_referrer_id, add_referral

router = Router()

@router.message(Command("start"))
async def cmd_start(message: Message):
    args = message.text.split()
    referrer_id = None
    if len(args) > 1:
        # Параметр после команды /start
        code = args[1]
        referrer_id = decode_referrer_id(code)
        # Сохраним в сессию, чтобы при регистрации использовать
        # Для этого используем FSM или просто передадим через контекст, но проще сохранить в data
        # Временно сохраним в message.bot_data (глобально) или лучше через state в дальнейшем.
        # Но так как регистрация происходит позже, нужно передать реферера в состояние.
        # Воспользуемся машиной состояний для хранения referrer_id.
        from aiogram.fsm.context import FSMContext
        # Однако здесь у нас нет доступа к state. Лучше обработать в отдельном хэндлере регистрации.
        # Поэтому пока просто сохраним в bot_data (не рекомендуется) или передадим через параметр.
        # Для упрощения предлагаю в момент регистрации проверять, есть ли код в deep link.
        # Это требует хранения реферера во временном хранилище, связанном с пользователем.
        # Простой вариант: при /start с реферальным кодом запоминаем в FSM после нажатия кнопки.
        # Но пользователь может не сразу зарегистрироваться.
        # Компромисс: сохраняем в redis или в глобальном словаре. Для простоты пока не реализуем.
        # Поясним в ответе пользователю.
        await message.answer("Реферальная ссылка активирована. При регистрации вы будете приглашены этим пользователем.")
    
    await message.answer(
        "👋 Добро пожаловать!\n\n"
        "💰 **Срочный выкуп** – оставьте заявку, перекупы предложат цену.\n"
        "🔄 **Я перекуп** – участвуйте в выкупе и продавайте технику.\n"
        "🛍 **Купить технику** – просмотр объявлений от перекупов.\n"
        "🔔 **Подписки** – настройте уведомления о новых лотах.\n"
        "💸 **Реферальная программа** – приглашайте друзей и получайте бонусы.\n\n"
        "Выберите действие:",
        reply_markup=main_menu(),
        parse_mode="Markdown"
    )

